Wandlend langs haar grijze muren,
Werd mijn hart op ’t diepst geroerd:
’k Was in ’s trotschen Alva’s dagen,
Toen zij ’t gruwzaamst schouwspel zagen,
Eer ik ’t wist teruggevoert.

